public class Student {
    private String name;
    private int nim;
    private String email;

    public void setName (String name) {
        this.name = name;
    }

    public String getName () {
        return name;
    }

    public void setNim (int nim) {
        this.nim = nim;
    }

    public int getNim () {
        return nim;
    }

    public void setEmail (String email) {
        this.email = email;
    }

    public String getEmail () {
        return email;
    }
}
